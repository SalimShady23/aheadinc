                            <div class="bold-text">   
                                The patients of the community of Maruku, Tanzania have a plethora of unmet health care needs. 
                                Chief among them is access to orthopedic surgery for joint restoration surgery.  
                                It’s estimated that less than 10% of the population of this entire region of Northern Tanzania, 
                                in need of this surgery, will receive it.  Women Orthopaedist Global Outreach (WOGO) has an answer. 
                                Since 2009, this amazing group of gifted surgeons and health care professionals have been traveling 
                                the globe performing life improving joint replacement surgeries. Elvira Williams, our Emeritus Exec. 
                                Director of AHEAD, Inc., coordinated services and volunteered with WOGO to provide services to seniors 
                                in Tanzania in 2014. Seven female surgeons completed 48 knee replacements in 4.5 days at ALMC Hospital 
                                in Arusha, Tanzania. These seven surgeons provided free surgical care to older women and men in Arusha 
                                who have suffered for many years in pain, some had not been able to walk for years. 
                                The WOGO surgeons partner with marginalized rural communities, perform pre-trip sessions with patients, 
                                local surgeons, and healthcare professionals to coordinate patient and surgical logistical details. 
                                They work with local medical professionals to screen, evaluate and take x-rays of potential surgical 
                                candidates, to plan their surgeries with greater precision and assemble appropriate supplies, 
                                including specific sizes of joint implants. The surgeons and the support team spend a week operating, 
                                doing post-operative recovery and rehabilitation. All surgeries are performed during the first few days 
                                of the trip affording the WOGO team time for on hand outcome assessment and to treat any complications. 
                                Their goal: perform as many joint reconstruction surgeries, as time, and donations allow, 
                                with a specific focus on pre-operative screening, excellent intraoperative care, efficient, 
                                effective post-operative recovery and rehabilitation, and patient hand off to local providers. 
                            </div>
                            <div class="bold-text">
                                WOGO is planning their mission trip for 2025 to return to Arusha Lutheran Medical Center (ALMC) in Arusha, Tanzania. 
                                They anticipate successfully reconstructing and rehabilitating ~ 50 knees. AHEAD, Incorporated is partnering with WOGO-Arusha Mission Project Medical Director 
                                and friend of AHEAD, Rinelda Horton, MD, an orthopaedic surgeon and her WOGO team to bring patients from 
                                Maruku and the Bukoba District to Arusha for total joint reconstruction. The WOGO care treatment/recovery 
                                arm of this partnership is free. Maruku, however is not close to Arusha, it is 361 miles away, 
                                and the patients will not have the resources to afford the travel cost, nor any pre-testing costs for labs, 
                                X-rays or incidental costs. Financial sponsorships and support from AHEAD, Inc., is needed to provide grants 
                                that will cover surgical candidate identification, preoperative testing and travel of twenty Maruku patients 
                                to and from Arusha, and provide for an AHEAD/Maruku designated patient liaison/care coordinator for the 
                                Maruku patients.
