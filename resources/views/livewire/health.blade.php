                                </ul>
                            </div>
                            <div class="bold-text">   
                                Naeema had complications with a previous childbirth, so when she was in labor, she and her sister walked to the newly constructed health center. 
                                Along the way, her contractions became really severe, so she stooped on the side of the dirt road and delivered the baby right there. 
                                A gentleman on a motorcycle picked her up and drove her down the road to The Maruku Health Center. 
                                The doctors and the team were able to care for her and monitor her vital signs in a safe, sterile environment at the Maruku Health Center.  
                                Prior to this, she would have had to travel an additional 45 minutes, by car to receive medical care, and would likely have died in route. 
                            </div>
                            <div class="bold-text">
                                As of August 2023, the doctors performed their first C-Section at the health center. Surgeries are now able to be performed due to benevolent 
                                generosity which enabled the purchase of a Universal Anesthesia Machine, AEDs, an Ultrasound and other equipment and supplies. During the height of the COVID outbreak, 
                                Maruku Health Center was one of the few rural health centers with the capacity to provide COVID Vaccines due to adequate refrigeration which also donated by <strong>AHEAD, Inc.</strong>
                            </div>
                            <div class="bold-text">
                                In spite of recent improvements in Maternal and Infant Mortality throughout Tanzania, data still shows an 
                                Infant Mortality Rate of 34/1000 live births and maternal loss of life in the first 42 days after birth 220/100,000, 
                                still amongst the highest in the world.
                            </div>
                            <div class="text">
                                Healthcare for mothers and infants is thwarted by:
                            </div>
                            <ul class="check-list">
                                <li>Late reporting of pregnant women to health care facilities (prenatal care)</li>
                                <li>Inadequate birth preparation and out-of-healthcare facility birth</li>
                                <li>Inadequate referrals hampered by transportation and communication</li>
                                <li>Shortage of skilled providers</li>
                                <li>Inadequate use of maternal and fetal data during labor (partograms)</li>
                                <li>Overall need for better data retrieval, collection, and compilation</li>
                                <li>Ongoing malnutrition, stunted growth, and failure to thrive in young children</li>
                            </ul>
                            <div class="bold-text">
                                In 2020, <strong>AHEAD, Inc.</strong> and <strong>AHEAD Tanzania</strong> expanded care at the 
                                Maruku Health Center to include the construction of a pediatric and two maternal 
                                wards with inpatient care. <strong>AHEAD, Inc.</strong> will continue to partner with the Maruku community in the further development of the Maruku Health Center as a state-of-the-art health center, and provide the health care resources necessary for mothers, infants, children, and families to thrive from birth, the early years of childhood, and throughout their lives.
                                Medical, healthcare and education professionals understand that thriving children need a healthy start to life. Achieving this start requires good prenatal care, safe births, high-quality neonatal care, and optimal care for mothers who will birth and live to care for their children. Children need their mothers at their sides. Malnutrition and stunted growth need to be fought with breast feeding and nutrition education.
                            </div>
                            <div class="bold-text">
                                <strong>AHEAD, Inc.</strong> and <strong>AHEAD Tanzania</strong> and local partners plan to replicate or expand maternal and infant services
                                through satellite clinics established in other communities in Rural Bukoba District as part of its long-term strategic goal. 
                                Strategic plans and goals include continuing health care beyond birth for women and 
                                young children by providing education on nutrition, breastfeeding, vaccination, dental care, 
                                and disease prevention, and educating new health care providers from the community who will participate in a village outreach program
                                <strong>AHEAD, Inc.</strong> seeks to provide the following:
                            </div>
                            <ul class="check-list">
                                <li>
                                    Establish electronic medical record (EMR) systems, creating direct and adequate 
                                    communication with the regional referral hospital in Bukoba as well as larger referral centers, and a way of
                                    compiling health care data to provide accurate needs and assessments
                                </li>
                                <li>
                                    Establish direct lines of communication between expectant and postpartum mothers with health
                                    care providers at the Maruku Health Center – prenatal tools for all mothers will include cell
                                    phones to support telemedicine, equipment to monitor blood pressure and weight, and
                                    educational materials on healthy pregnancies, nutrition, breastfeeding and warning signs
                                </li>
                                <li>
                                    Increase the number of prenatal visits to greater than 4, which has been shown to improve
                                    outcomes. Lessons have been learned from COVID on the value of Telemedicine visits which will
                                    provide much easier access to prenatal care and add to existing regimens
                                </li>
                                <li>Upgrade Maruku Health Center’s operating suite to provide for successful emergency deliveries</li>
                                <li>
                                    Establish a local ambulance system for rapid referral and transport to and from the Maruku
                                    Health Center from villages as well as emergency transportation for stabilizing and transporting
                                    high risk infants and mothers to the Bukoba Regional Referral Hospital
                                </li>
                                <li>Establish safe blood banking capabilities at Maruku Health Center</li>
                                <li>
                                    Establish an on-site residential option for high-risk pregnant women in the final weeks of
                                    pregnancy that provides them with more potentially lifesaving, consistent care than can be
                                    provided in their villages
                                </li>
                                <li>
                                    Provide training of Medical Assistants and Birth Assistants from the people living in the
                                    community who will travel from Maruku Health Center to villages for prenatal clinics, pediatric
                                    check-ups, nutritional education, and also compile data to identify areas of need
                                </li>
                                <li>
                                    Establish Maruku Health Center as a vital part of the community, with educational resources,
                                    health care, and an established center for dental care for pregnant women, mothers, infants,
                                    small children, and the other people of Maruku Ward
                                </li>
                            </ul>
